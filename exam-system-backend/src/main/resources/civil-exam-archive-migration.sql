INSERT INTO question
(course_id, question_type, content, options_json, answer, analysis, difficulty, score, knowledge_tag,
 exam_year, exam_scope, province, paper_type, source_ref, create_user_id)
SELECT
  @civil_course_id,
  seed.question_type,
  seed.content,
  seed.options_json,
  seed.answer,
  seed.analysis,
  seed.difficulty,
  seed.score,
  seed.knowledge_tag,
  seed.exam_year,
  seed.exam_scope,
  seed.province,
  seed.paper_type,
  seed.source_ref,
  @civil_owner_id
FROM civil_archive_seed seed
WHERE @civil_course_id IS NOT NULL
  AND @civil_owner_id IS NOT NULL
  AND NOT EXISTS (
    SELECT 1 FROM question existing
    WHERE existing.course_id = @civil_course_id
      AND existing.content = seed.content
      AND existing.exam_year = seed.exam_year
  );
